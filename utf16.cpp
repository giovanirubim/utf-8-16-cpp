long long unsigned utf16::utf8_length(const char* src) {
	long int length = 0;
	unsigned char byte, state = 1, last;
	for (;;) {
		byte = *src++;
		last = state;
		state = state_map[state][byte];
		if (!state) break;
		if (state == 1) ++ length;
	}
	return (src[-1] || last != 1)? -1: length;
}

wchar_t* utf16::parse_utf8(wchar_t dst[], const char* src) {
	wchar_t* ptr = dst;
	unsigned char byte, state = 1, last;
	unsigned long int buffer;
	for (;;) {
		byte = *src++;
		last = state;
		state = state_map[state][byte];
		if (!state) break;
		switch (state) {
			case 1:
				if (last == 1) {
					*ptr++ = byte;
				} else {
					buffer <<= 6;
					buffer |= byte & 0b00111111;
					*ptr++ = buffer;
				}
			break;
			case 2:
			case 3:
				buffer <<= 6;
				buffer |= byte & 0b00111111;
			break;
			case 4:
				buffer = byte & 0b00011111;
			break;
			case 5:
				buffer = byte & 0b00001111;
			break;
			case 6:
				buffer = byte & 0b00000111;
			break;
		}
	}
	*ptr++ = 0;
	if (src[-1] || last != 1) {
		*dst = 0;
		return nullptr;
	}
	return dst;
}

